package co.green.model;

public class GreenLogin {
	
	String u_id;
	String u_pw;
	String u_Nname;
	String u_jumin;
	String u_phone;
	String u_plant;
	
	
	public String getU_id() {
		return u_id;
	}
	public void setU_id(String u_id) {
		this.u_id = u_id;
	}
	public String getU_pw() {
		return u_pw;
	}
	public void setU_pw(String u_pw) {
		this.u_pw = u_pw;
	}
	public String getU_Nname() {
		return u_Nname;
	}
	public void setU_Nname(String u_Nname) {
		this.u_Nname = u_Nname;
	}
	public String getU_jumin() {
		return u_jumin;
	}
	public void setU_jumin(String u_jumin) {
		this.u_jumin = u_jumin;
	}
	public String getU_phone() {
		return u_phone;
	}
	public void setU_phone(String u_phone) {
		this.u_phone = u_phone;
	}
	public String getU_plant() {
		return u_plant;
	}
	public void setU_plant(String u_plant) {
		this.u_plant = u_plant;
	}
	@Override
	public String toString() {
		return "GreenLogin [u_id=" + u_id + ", u_pw=" + u_pw + ", u_Nname=" + u_Nname + ", u_jumin=" + u_jumin
				+ ", u_phone=" + u_phone + "]";
	}
	
	
	
	

}
